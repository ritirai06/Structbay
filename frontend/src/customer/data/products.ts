export const PRODUCTS: Product[] = _PRODUCTS.map((p, i) => {
  // Dynamically assign cities to products based on index so it varies by city
  const availableCities = ["Bengaluru"];
  if (i % 2 === 0) availableCities.push("Chennai");
  if (i % 3 !== 0) availableCities.push("Hyderabad");
  
  // Ensure some specific categories are everywhere or restricted to test filtering
  if (p.category === "cement") {
    availableCities.push("Chennai", "Hyderabad"); // Cement available everywhere
  }
  
  // Deduplicate array
  return { ...p, availableCities: Array.from(new Set(availableCities)) };
});

export function getProductById(id: string) {
  return PRODUCTS.find((p) => p.id === id);
}

export function getProductsByCategory(category: string) {
  return PRODUCTS.filter((p) => p.category === category.toLowerCase());
}
